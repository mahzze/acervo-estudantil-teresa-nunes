const addModalCursos = () => {
  const modal = document.querySelector('div#modalCursos');
  return modal.style.display = (modal.style.display == "block") ? "none" : "block";
}

const closeModalCursos = () => {
  const modal = document.querySelector('div#modalCursos');
  return modal.style.display = (modal.style.display == "block") ? "none" : "block";
}
